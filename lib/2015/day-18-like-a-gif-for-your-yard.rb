require_relative '../util'

class LightGrid
  def initialize(input)
    @lights = input.split.map do |line|
      line.chars.map do |char|
        char == '#'
      end
    end
  end

  def size
    @lights.length
  end

  def to_s
    @lights.map do |line|
      line.map do |light|
        light ? '#' : '.'
      end.join
    end.join "\n"
  end

  def on?(row, col)
    row.in?(0...size) &&
      col.in?(0...size) &&
      @lights[row][col]
  end

  def num_neighbors_on(row, col)
    (row-1..row+1).sum do |r|
      (col-1..col+1).count do |c|
        [r,c] != [row,col] && on?(r, c)
      end
    end
  end

  def next_on?(row, col)
    n = num_neighbors_on(row, col)
    if on?(row, col)
      n == 2 || n == 3
    else
      n == 3
    end
  end

  def animate(steps = 1)
    steps.times do
      @lights = size.times.map do |row|
        size.times.map do |col|
          next_on?(row, col)
        end
      end
    end
    self
  end

  def num_on
    size.times.sum do |r|
      size.times.count do |c|
        on?(r, c)
      end
    end
  end
end

class BrokenCornersLightGrid < LightGrid
  def initialize(input)
    super
    corner_coords.each { |r,c| @lights[r][c] = true }
  end

  def corner_coords
    [0, size-1].product([0, size-1])
  end

  def next_on?(row, col)
    [row,col].in?(corner_coords) || super
  end
end

if defined? DATA
  input = DATA.read
  grid = LightGrid.new(input)
  puts grid.animate(100)
  puts grid.num_on

  grid = BrokenCornersLightGrid.new(input)
  puts grid.animate(100)
  puts grid.num_on
end

