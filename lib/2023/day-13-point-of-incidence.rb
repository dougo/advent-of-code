class ValleyOfMirrors
  def self.parse(text)
    new(text.lines(chomp: true).join('\n').split('\n\n').map { Pattern.new(_1.split('\n')) })
  end

  def initialize(patterns)
    @patterns = patterns
  end
      
  attr :patterns

  class Pattern
    def initialize(lines)
      @lines = lines
    end

    attr :lines

    def numbers_of_diffs(array1, array2)
      array1.zip(array2).map do |line1, line2|
        line1.chars.zip(line2.chars).count { _1 != _2 }
      end
    end

    def rows_above_mirror(smudged: false)
      (1...lines.length).find do |rows|
        above = lines[...rows].reverse
        below = lines[rows..]
        if rows > lines.length / 2
          above = above[...below.length]
        else
          below = below[...above.length]
        end
        if smudged
          numbers_of_diffs(above, below).sum == 1
        else
          above == below
        end
      end
    end

    def cols_left_of_mirror(...)
      transposed = lines.map(&:chars).transpose.map(&:join)
      self.class.new(transposed).rows_above_mirror(...)
    end
  end

  def summarize_pattern_notes(...)
    patterns.sum do |pattern|
      pattern.cols_left_of_mirror(...) || pattern.rows_above_mirror(...) * 100
    end
  end
end

if defined? DATA
  valley = ValleyOfMirrors.parse(DATA.read)
  puts valley.summarize_pattern_notes
  puts valley.summarize_pattern_notes(smudged: true)
end

