require_relative '../util'

class StepCounter
  def self.parse(text)
    lines = text.lines(chomp: true).map(&:chars)
    new(Grid.new(lines))
  end

  def initialize(grid)
    @grid = grid
  end

  attr :grid

  class Grid
    def initialize(rows)
      @rows = rows
    end

    attr :rows

    def height = rows.length
    def width = rows.first.length

    def has?(pos)
      pos => row, col
      (0...height).include?(row) && (0...width).include?(col)
    end  

    def [](pos)
      pos => row, col
      rows[row][col] if has?(pos)
    end

    def []=(pos, contents)
      pos => row, col
      rows[row][col] = contents if has?(pos)
    end

    def each_position
      (0...height).each do |row|
        (0...width).each do |col|
          yield Position[row, col]
        end
      end
    end

    def dup
      self.class.new(rows.map(&:dup))
    end
  end

  def start_pos
    grid.each_position { return _1 if grid[_1] == 'S' }
  end

  def neighbors(pos)
    [NORTH, EAST, SOUTH, WEST].map { pos.move(_1) }
  end

  def expand_steps(grid)
    new_grid = grid.dup
    grid.each_position do |pos|
      if grid[pos] == '.'
        neighbor_steps = neighbors(pos).filter_map do |n|
          s = grid[n]
          s if Integer === s
        end
        if neighbor_steps.any?
          new_grid[pos] = neighbor_steps.min + 1
        end
      end
    end
    new_grid
  end

  def num_plots_in_steps(steps)
    new_grid = grid.dup
    new_grid[start_pos] = 0
    steps.times do |i|
      new_grid = expand_steps(new_grid)
    end
    num = 0
    new_grid.each_position do |pos|
      s = new_grid[pos]
      num += 1 if Integer === s && s.even?
    end
    num
  end
end

if defined? DATA
  input = DATA.read
  obj = StepCounter.parse(input)
  puts obj.num_plots_in_steps(64)
end

