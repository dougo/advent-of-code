require_relative 'util'

class LightGrid
  def initialize(input)
    @lines = input.split
    @lines[0][0] = '#'
    @lines[0][99] = '#'
    @lines[99][0] = '#'
    @lines[99][99] = '#'
  end

  attr_reader :lines

  def animate(steps = 1)
    steps.times do
      new_lines = Array.new(100) { '.' * 100 }
      100.times.each do |row|
        100.times.each do |col|
          new_lines[row][col] = new_light(row, col)
        end
      end
      @lines = new_lines
    end
  end

  def new_light(row, col)
    n = neighbors(row, col)
    if (row == 0 || row == 99) && (col == 0 || col == 99)
      '#'
    elsif @lines[row][col] == '#'
      n == 2 || n == 3 ? '#' : '.'
    else
      n == 3 ? '#' : '.'
    end
  end

  def neighbors(row, col)
    (row-1..row+1).sum do |r|
      (col-1..col+1).count do |c|
        (r != row || c != col) &&
          r >= 0 && r < 100 &&
          c >= 0 && c < 100 &&
          @lines[r][c] == '#'
      end
    end
  end

  def how_many_on?
    100.times.sum { |r| 100.times.count { |c| @lines[r][c] == '#' } }
  end
end

if defined? DATA
  grid = LightGrid.new(DATA.read)
  grid.animate(100)
  puts grid.lines
  puts grid.how_many_on?
end

